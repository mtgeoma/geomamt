      SUBROUTINE D02NNR(ITHINK)
C     MARK 12 RELEASE. NAG COPYRIGHT 1986.
C     .. Scalar Arguments ..
      INTEGER           ITHINK
C     .. Scalars in Common ..
      INTEGER           IDEV, ITRACE
C     .. Local Scalars ..
      CHARACTER*80      REC
C     .. External Subroutines ..
      EXTERNAL          X04BAF
C     .. Common blocks ..
      COMMON            /AD02NM/ITRACE, IDEV
C     .. Save statement ..
      SAVE              /AD02NM/
C     .. Executable Statements ..
      GO TO (20,40,60,80,100,120,140,160,180)
     *       ITHINK
   20 WRITE (REC,FMT=99999)
      CALL X04BAF(IDEV,REC)
      WRITE (REC,FMT=99998)
      CALL X04BAF(IDEV,REC)
      GO TO 200
   40 WRITE (REC,FMT=99997)
      CALL X04BAF(IDEV,REC)
      WRITE (REC,FMT=99996)
      CALL X04BAF(IDEV,REC)
      GO TO 200
   60 WRITE (REC,FMT=99995)
      CALL X04BAF(IDEV,REC)
      WRITE (REC,FMT=99994)
      CALL X04BAF(IDEV,REC)
      GO TO 200
   80 WRITE (REC,FMT=99993)
      CALL X04BAF(IDEV,REC)
      WRITE (REC,FMT=99992)
      CALL X04BAF(IDEV,REC)
      GO TO 200
  100 WRITE (REC,FMT=99991)
      CALL X04BAF(IDEV,REC)
      WRITE (REC,FMT=99990)
      CALL X04BAF(IDEV,REC)
      GO TO 200
  120 WRITE (REC,FMT=99989)
      CALL X04BAF(IDEV,REC)
      WRITE (REC,FMT=99988)
      CALL X04BAF(IDEV,REC)
      GO TO 200
  140 WRITE (REC,FMT=99987)
      CALL X04BAF(IDEV,REC)
      WRITE (REC,FMT=99986)
      CALL X04BAF(IDEV,REC)
      GO TO 200
  160 WRITE (REC,FMT=99985)
      CALL X04BAF(IDEV,REC)
      WRITE (REC,FMT=99984)
      CALL X04BAF(IDEV,REC)
      GO TO 200
  180 WRITE (REC,FMT=99983)
      CALL X04BAF(IDEV,REC)
      WRITE (REC,FMT=99982)
      CALL X04BAF(IDEV,REC)
      WRITE (REC,FMT=99981)
      CALL X04BAF(IDEV,REC)
  200 CONTINUE
      RETURN
C
99999 FORMAT (' SPARSE DETAILS: THE USER COULD SUPPLY LESS INTEGER')
99998 FORMAT ('  WORKSPACE , PROBABLY WITHOUT AFFECTING THE PERFORMANCE'
     *  )
99997 FORMAT (' SPARSE DETAILS: THE USER COULD SUPPLY LESS REAL')
99996 FORMAT ('  WORKSPACE , PROBABLY WITHOUT AFFECTING THE PERFORMANCE'
     *  )
99995 FORMAT (' SPARSE DETAILS: PERFORMANCE WOULD PROBABLY IMPROVE')
99994 FORMAT ('  IF MORE INTEGER WORKSPACE WERE PROVIDED')
99993 FORMAT (' SPARSE DETAILS: AND MORE REAL WORKSPACE WERE')
99992 FORMAT ('  PROVIDED')
99991 FORMAT (' SPARSE DETAILS: AND THE STORAGE DIVISION PARAMETER')
99990 FORMAT ('  ISPLIT WERE ALTERED')
99989 FORMAT (' SPARSE DETAILS: PERFORMANCE WOULD PROBABLY IMPROVE')
99988 FORMAT ('  IF ISPLIT WERE ALTERED')
99987 FORMAT (' SPARSE DETAILS: PERFORMANCE WOULD PROBABLY IMPROVE')
99986 FORMAT ('  IF MORE REAL WORKSPACE WERE PROVIDED')
99985 FORMAT (' SPARSE DETAILS: AND MORE INTEGER WORKSPACE WERE')
99984 FORMAT ('  PROVIDED')
99983 FORMAT (' SPARSE DETAILS: THE JACOBIAN MATRIX IS IRREDUCIBLE ,')
99982 FORMAT ('  IT WOULD PROBABLY BE MORE EFFICIENT ON RUNS OF A ')
99981 FORMAT ('  SIMILAR PROBLEM TO SET LBLOCK = .FALSE.')
      END
