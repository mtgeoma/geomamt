        subroutine ps1win(x,nch,npts,lwinst,w)
 
        real x(nch,npts),w(npts)
        logical lwinst
 
        parameter(p1=5.3476939016920851e-11,p2=2.2654256220146656e-9,
     1  p3=7.8075102004229667e-8,p4=2.1373409644281953e-6,
     2  p5=4.5094847544714943e-5,p6=7.0498957221483167e-4,
     3  p7=7.7412693304064753e-3,p8=5.5280627452077586e-2,
     4  p9=2.2753754228751827e-1,p10=4.3433904277546202e-1,
     5  p11=2.2902051859068017e-1)
 
        if(lwinst) then
           np2=(npts+1)/2
           d=sqrt(2./float(npts))
           do 20 i=1,np2
           xt=(2.*i-1.)/npts-1.
           u=(1.-xt)*(1.+xt)
           w(i)=((((((((((p1*u+p2)*u+p3)*u+p4)*u+p5)*u+p6)*u+p7)*u+p8)
     1        *u+p9)*u+p10)*u+p11)*d
           w(npts-i+1) = w(i)
