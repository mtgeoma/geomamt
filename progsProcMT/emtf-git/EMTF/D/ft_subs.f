c**********************************************
c
        subroutine fftnp2(n,m,x,z,wsave)
c
c
c       computes discreet fourier transforms of time series
c       in x; variables:
c               x(m,n) = array of m channels of time series
c                 of length n=2**l2n on input;  on output
c                 x contains the transform in a compact form
c                 using the symmetry implied by the real
c                 form of x; the output form is:
c                       x(j,2k-1)= real part of transform
c                               for frequency k/n
c                       x(j,2k)= imag. part of tran.
c                               for freq. k/n
c                 negative frequencies are obtained as complex
c                 conjugates of pos. frequencies
c               z(2,n) =  work space; should be z(2,2n)
C               wsave(*) = array containing prime factors, tables
c                         for FFT
        real x(m,n),z(2,n),wsave(*)
        
c
        n2=int(n/2+.5)
        
c
          do 100 j=1,m,2
c
        if (j.eq.m) go to 20
c
           do 15 i=1,n
           z(1,i)=x(j,i)
           z(2,i)=x(j+1,i)
15         continue
           go to 30
c
20         do 25 i=1,n
           z(1,i)=x(m,i)
           z(2,i)=0.
25         continue
c
30      continue
           
           call CFFTF(n,z,wsave)

        if(j.eq.m)go to 50
c
           do 40 i=2,n2+1
           mi=n-i+2
           k2=2*(i-1)
           k1=k2-1
           x(j,k1)=(z(1,i)+z(1,mi))/2.
           x(j,k2)=(z(2,i)-z(2,mi))/2.
           x(j+1,k1)=(z(2,i)+z(2,mi))/2.
           x(j+1,k2)=(z(1,mi)-z(1,i))/2.
40         continue
        go to 60
 
50      continue
 
           do 55 i=2,n2+1
           k2=2*(i-1)
           k1=k2-1
           x(m,k1)=z(1,i)
           x(m,k2)=z(2,i)
55         continue
 
60      continue
100     continue
        return
        end
